#!/usr/bin/env node
// Node.js entry point for the OBS MCP stdio server.
//
// The dart2js-compiled runtime in `dist/obs_mcp_server.runtime.js` was
// emitted assuming a browser-like global environment — it references the
// `self` and `window` globals (e.g. inside `package:web_socket_channel` and
// `dart:async` adapters). Loading it directly under Node.js throws
// `ReferenceError: self is not defined` the first time the StreamController
// machinery is exercised, which silently breaks JSON-RPC responses on stdout
// (the failure is swallowed by `dart_mcp`'s peer sink).
//
// To keep the dart2js artifact portable, this wrapper:
//   1. Polyfills the missing browser globals (`self`, `window`) BEFORE the
//      runtime is evaluated.
//   2. Holds the Node.js event loop open with a long-running interval timer
//      so the dart2js `main()` (which only attaches stdin listeners) does not
//      let the process exit prematurely.
//   3. Cleans up the keep-alive timer on SIGINT / SIGTERM / stdin EOF so the
//      process shuts down cleanly when the MCP host disconnects.
//
// This file is hand-maintained — it is NOT regenerated by `scripts/build.sh`.
// Only `dist/obs_mcp_server.runtime.js` is rebuilt by the dart2js step.

import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

// ---------------------------------------------------------------------------
// 1. Polyfill browser globals expected by the dart2js runtime
// ---------------------------------------------------------------------------
const g = /** @type {any} */ (globalThis);
if (typeof g.self === 'undefined') g.self = g;
if (typeof g.window === 'undefined') g.window = g;

// ---------------------------------------------------------------------------
// 2. Keep the event loop alive while the MCP server is running
// ---------------------------------------------------------------------------
// dart2js only attaches `process.stdin.on('data', ...)` listeners. Without an
// active timer/handle, Node.js may exit when stdin is paused. A 1-day timer
// is effectively infinite for practical sessions and is cheap.
const keepAlive = setInterval(() => {}, 1 << 30);

const shutdown = (code = 0) => {
  clearInterval(keepAlive);
  process.exit(code);
};

process.on('SIGINT', () => shutdown(130));
process.on('SIGTERM', () => shutdown(143));
process.stdin.on('end', () => shutdown(0));
process.stdin.on('close', () => shutdown(0));

// ---------------------------------------------------------------------------
// 3. Load the dart2js-compiled runtime (self-executes via its `main()`)
// ---------------------------------------------------------------------------
const here = dirname(fileURLToPath(import.meta.url));
const runtime = resolve(here, '../dist/obs_mcp_server.runtime.js');
await import(runtime);
